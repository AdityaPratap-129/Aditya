class Solution
{
    public:
    //Function to rotate a linked list.
    Node* rotate(Node* head, int k)
    {
        // Your code here
            Node *ptr=head;
            Node *cur=head;
            Node *prev=head;
            int c=0,m=1;
            while(c!=k && cur!=NULL)
            {
                prev=cur;
                c++;
                cur=cur->next;
            }
            while(ptr->next!=NULL)
            {
                m++;
                ptr=ptr->next;
            }
            if(m==k)
            {
                return head;
            }
            prev->next=NULL;
            ptr->next=head;
            head=cur;
            return head;
        
    }
    
};
    


